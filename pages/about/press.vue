<template>
  <Container id="press">
    <Article>
      <div class="text">
        <h1>Press</h1>
      </div>
      <div class="box-wrapper">
        <ul>
          <li v-for="data in json.press">
            <template v-if="data.url">
              <a :href="data.url" target="_blank">{{ data.text }}</a>
            </template>
            <template v-else>
              {{ data.text }}
            </template>
          </li>
        </ul>
      </div>
    </Article>
  </Container>
</template>

<script>
import { mapActions } from 'vuex'
import seo from "~/content/data/seo.json"
import json from "~/content/data/press.json"
import Cluster from '~/components/Cluster'
import Container from '~/components/Container'
import Article from '~/components/Article'
import PrevNextButtons from '~/components/PrevNextButtons'

export default {
  head() {
    return {
      title: `${seo.shortTitle} | ${this.$t('secondaryNavbar.press')}`
    }
  },
  nuxtI18n: {
    paths: {
      en: '/about/press',
      de: '/uber/press'
    }
  },
  components: {
    Cluster,
    Container,
    Article,
    PrevNextButtons
  },
  async asyncData() {
    // create context via webpack to map over all blog pages
    const allPages = await require.context("~/content/about/", true, /\.md$/)
    const pages = allPages.keys().map(key => {
      // give back the value of each about context
      return allPages(key)
    })
    return {
      pages: [
        ...pages,
        {
          attributes: {
            title: 'Business Partner',
            en_title: 'Business Partner',
            de_title: 'Business Partner',
            position: 4
          },
          // attributes: {
          //   title: 'Press',
          //   en_title: 'Press',
          //   de_title: 'Press',
          //   position: 5
          // },
        }
      ],
      json: json
    }
  },
  mounted() {
    this.setPages(this.$data.pages)
    this.setPagesPrefix("about")
  },
  methods: {
    ...mapActions(['setPages', 'setPagesPrefix'])
  }
}
</script>
